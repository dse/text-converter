const UPPER = Array.from("рҹ„°рҹ„ұрҹ„Ірҹ„ірҹ„ҙрҹ„өрҹ„¶рҹ„·рҹ„ёрҹ„№рҹ„әрҹ„»рҹ„јрҹ„Ҫрҹ„ҫрҹ„ҝрҹ…Җрҹ…Ғрҹ…Ӯрҹ…ғрҹ…„рҹ……рҹ…Ҷрҹ…Үрҹ…Ҳрҹ…ү");
const LOWER = Array.from("рҹ„°рҹ„ұрҹ„Ірҹ„ірҹ„ҙрҹ„өрҹ„¶рҹ„·рҹ„ёрҹ„№рҹ„әрҹ„»рҹ„јрҹ„Ҫрҹ„ҫрҹ„ҝрҹ…Җрҹ…Ғрҹ…Ӯрҹ…ғрҹ…„рҹ……рҹ…Ҷрҹ…Үрҹ…Ҳрҹ…ү");

